"""
Operadores relacionais
"""
x = 5;
y = 7;

#Maior que 

print(x > y); #false
print()

#Menor que 
print(x < 7) #true
print();

#Igualdade
print(x == y); #false
print()

#Maior ou Igual a
print(x >= y); #false
print()

# Menor ou Igual 
print(x <= y) #true
print()

#Diferente de
print(x !=y); #true
print();